export const findRelevantInfo = (question: string, doshaType: string, category: string): string => {
  const doshaInfo = ayurvedaKnowledge[doshaType as keyof typeof ayurvedaKnowledge];
  if (!doshaInfo) return "Извините, информация не найдена.";

  const questionLower = question.toLowerCase();
  
  if (questionLower.includes('специ')) {
    return doshaInfo["специи"];
  }
  
  if (questionLower.includes('продукт') || questionLower.includes('еда') || questionLower.includes('питан')) {
    return doshaInfo["продукты"];
  }
  
  if (questionLower.includes('режим') || questionLower.includes('время')) {
    return doshaInfo["питание"];
  }
  
  // If no specific match is found, return relevant category info
  return doshaInfo[category as keyof typeof doshaInfo] || 
    `Для вашей ${doshaType} доши я могу рассказать о:
    - Рекомендуемых специях
    - Продуктах питания
    - Режиме питания
    
    О чем бы вы хотели узнать подробнее?`;
};